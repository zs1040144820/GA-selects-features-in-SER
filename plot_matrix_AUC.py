def plot_matrix(FN, FP, TP,TN,labels_name, title=None, thresh=50, axis_labels=None):
    '''
    plot_matrix函数参数包括：
    y_true样本的真实标签，为一向量
    y_pred样本的预测标签，为一向量，与真实标签长度相等
    labels_name样本在数据集中的标签名，如在示例中，样本的标签用0, 1, 2表示，则此处应为[0, 1, 2]
    title=None图片的标题
    thresh=0.8临界值，大于此值则图片上相应位置百分比为白色
    axis_labels=None最终图片中显示的标签名，如在示例中，样本标签用0, 1, 2表示分别表示失稳、稳定与潮流不收敛，我们最终图片中显示后者而非前者，则可令此参数为[‘unstable’, ‘stable’, ‘non-convergence’]
    '''

    cm = [[0,0],[0,0]]
    cm[0][0] = TP
    cm[1][0] = FP
    cm[0][1] = FN
    cm[1][1] = TN


    # 画图，如果希望改变颜色风格，可以改变此部分的cmap=pl.get_cmap('Blues')处
    pl.imshow(cm, interpolation='nearest', cmap=pl.get_cmap('Blues'))
    pl.colorbar()  # 绘制图例
    # 图像标题
    if title is not None:
        pl.title(title)
    # 绘制坐标
    num_local = np.array(range(len(labels_name)))
    if axis_labels is None:
        axis_labels = labels_name
    pl.xticks(num_local, axis_labels, rotation=45)  # 将标签印在x轴坐标上， 并倾斜45度
    pl.yticks(num_local, axis_labels)  # 将标签印在y轴坐标上
    pl.ylabel('True label')
    pl.xlabel('Predicted label')
    # 将百分比打印在相应的格子内，大于thresh的用白字，小于的用黑字
    for i in range(np.shape(cm)[0]):
        for j in range(np.shape(cm)[1]):
                pl.text(j, i, str(cm[i][j])+"%",
                        ha="center", va="center",
                        color="black" if cm[i][j] < thresh else "white")  # 如果要更改颜色风格，需要同时更改此行
    # 显示
    pl.show()





def plot_auc(y_predicted1, y_true1, y_predicted2, y_true2, y_predicted3, y_true3, y_predicted4, y_true4, y_predicted5, y_true5):
    Font = {'size': 18, 'family': 'Times New Roman'}

    label1 = y_true1
    label2 = y_true2
    label3 = y_true3
    label4 = y_true4
    label5 = y_true5
    fpr1, tpr1, thres1 = metrics.roc_curve(label1, y_predicted1)
    fpr2, tpr2, thres2 = metrics.roc_curve(label2, y_predicted2)
    fpr3, tpr3, thres3 = metrics.roc_curve(label3, y_predicted3)
    fpr4, tpr4, thres4 = metrics.roc_curve(label4, y_predicted4)
    fpr5, tpr5, thres5 = metrics.roc_curve(label5, y_predicted5)
    roc_auc1 = metrics.auc(fpr1, tpr1)
    roc_auc2 = metrics.auc(fpr2, tpr2)
    roc_auc3 = metrics.auc(fpr3, tpr3)
    roc_auc4 = metrics.auc(fpr4, tpr4)
    roc_auc5 = metrics.auc(fpr5, tpr5)

    plt.figure(figsize=(10, 10))
    plt.plot(fpr1, tpr1 , label='reinforcement learning_FN-0.00001 = %0.3f' % roc_auc1, color='Red')
    plt.plot(fpr2, tpr2, label='reinforcement learning_FN-0.0001 = %0.3f' % roc_auc2, color='green')
    plt.plot(fpr3, tpr3, label='reinforcement learning_FN-0.0006 = %0.3f' % roc_auc3, color='RoyalBlue')
    plt.plot(fpr5, tpr5, label='reinforcement learning_FN-0.006 = %0.3f' % roc_auc5, color='yellow')

    plt.plot(fpr4, tpr4, label='deep learning = %0.3f' % roc_auc4, color='black')


    plt.legend(loc='lower right', prop=Font)
    plt.plot([0, 1], [0, 1], 'r--')
    plt.xlim([0, 1])
    plt.ylim([0, 1])
    plt.ylabel('True Positive Rate', Font)
    plt.xlabel('False Positive Rate', Font)
    plt.tick_params(labelsize=15)
    plt.show()
    return abs(fpr1 - tpr1).max(), abs(fpr2 - tpr2).max(), abs(fpr3 - tpr3).max()


